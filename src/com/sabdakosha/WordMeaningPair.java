package com.sabdakosha;

public class WordMeaningPair {
    private String word;
    private String meaning;

    public WordMeaningPair(String word, String meaning) {
        this.word = word;
        this.meaning = meaning;
    }

    public String getWord() {
        return word;
    }

    public String getMeaning() {
        return meaning;
    }
}
